Future<void> _signUpWithEmailAndPassword(String email, String password) async {
  final String url = 'https://identitytoolkit.googleapis.com/v1/accounts:signUp?key=AIzaSyAPO1oxnliP55RQsiGabQXGmafTfPsPEO8';

  try {
    final response = await http.post(
      Uri.parse(url),
      headers: <String, String>{
        'Content-Type': 'application/json; charset=UTF-8',
      },
      body: jsonEncode(<String, String>{
        'email': email,
        'password': password,
        'returnSecureToken': 'true',
      }),
    );

    if (response.statusCode == 200) {
              Navigator.pushReplacement(
          context,
          MaterialPageRoute(builder: (context) => SignInScreen()),
        );
    } else {
      // Se a solicitação falhar, trate o erro aqui
      final responseData = json.decode(response.body);
      final String errorMessage = responseData['error']['message'];
      print('Erro durante o registro do usuário: $errorMessage');
       ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Erro durante a autenticação: $errorMessage'),
        ),
      );
    }
  } catch (e) {
    // Se ocorrer uma exceção durante a solicitação HTTP, trate-a aqui
    print('Erro durante a solicitação HTTP: $e');
  }
}

