  Future<void> _signInWithEmailAndPassword(String email, String password) async {
    final String url =
        'https://identitytoolkit.googleapis.com/v1/accounts:signInWithPassword?key=AIzaSyAPO1oxnliP55RQsiGabQXGmafTfPsPEO8';

    try {
      final response = await http.post(
        Uri.parse(url),
        headers: <String, String>{
          'Content-Type': 'application/json; charset=UTF-8',
        },
        body: jsonEncode(<String, String>{
          'email': email,
          'password': password,
          'returnSecureToken': 'true',
        }),
      );

      if (response.statusCode == 200) {
        // Autenticação bem-sucedida, navegar para a tela inicial
        Navigator.pushReplacement(
          context,
          MaterialPageRoute(builder: (context) => HomeScreen()),
        );
      } else {
        // Autenticação falhou, exibir mensagem de erro
        final responseData = json.decode(response.body);
        final String errorMessage = responseData['error']['message'];
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Erro durante a autenticação: $errorMessage'),
            backgroundColor: Colors.red,
          ),
        );
      }
    } catch (e) {
      // Exceção durante a solicitação HTTP
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Erro durante a solicitação HTTP: $e'),
          backgroundColor: Colors.red,
        ),
      );
    }
  }
